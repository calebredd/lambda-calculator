 const add=(number,secondNumber)=>{
  return number+secondNumber;
};
 const subtract=(number,secondNumber)=>{
  return number-secondNumber;
};
 const multiply=(number,secondNumber)=>{
  return number*secondNumber;
};
 const divide=(number,secondNumber)=>{
  return number/secondNumber;
};
const flip=(number)=>{
  return number*(-1);
}

export {add,subtract,multiply,divide, flip};
